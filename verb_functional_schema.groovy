General
    Verb moods // indicate a speaker's attitude, intent, or the reality of an action, generally falling into three to five types in English: indicative (facts), imperative (commands), conditional (contingencies), and subjunctive (hypotheticals/desires). These forms change how verbs behave to convey necessity, doubt, or certainty. 
        Indicative // States facts, opinions, or asks questions about reality (e.g., "She sings well.").
            Présent
                A1, A2, B1, B2
            Imparfait
                B1, B2
            Passé Composé
                A2, B1, B2
            Futur Simple
                B1, B2
            Plus-Que-Parfait
                B2
            Futur Antérieur
                B2
            Futur Proche // je vais manger (I am going to eat)
                B1, B2
        Imperative // Gives commands, requests, or directions (e.g., "Close the door.").
            L’impératif présent
                A1, A2, B1, B2
        Conditional // Expresses a state dependent on a condition, often using "if" (e.g., "I would go if I had money.").
            Conditionnel Présent
                B1, B2
            Conditionnel Passé
                B2
        Subjunctive // Expresses doubt, desire, hypothetical situations, or commands (e.g., "I wish I were there," "It is vital that he study.")
            Subjonctif Présent
                B2
            Subjonctif Passé
                B2
    Non-Finite Forms
        Infinitive // the most neutral version of a verb, “to (verb)” (like “to eat” or “to run”)
            L’infinitif présent
                A1, A2, B1, B2
            L’infinitif passé (avoir mangé / être allé).
                B1, B2
        Participle // the equivalent of the verb + -ing (present participle) or -ed (past participle) (non-finite verb forms that can function as adjectives or nouns)
            Le participe présent
                B1, B2
            Le participe passé
                A1, A2, B1, B2
        Le Gérondif (en parlant)
            B1, B2
