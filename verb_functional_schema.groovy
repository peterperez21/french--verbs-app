    Voice
        Active Voice // The subject performs the action (e.g., "Je mange la pomme.").
            A1, A2, B1, B2
        Passive Voice // The subject receives the action (e.g., "La pomme est mangée par moi."). Etre + past participle
            B1, B2
    Transitivity
        Transitive Verbs // require a direct object (e.g., "Je mange une pomme.").
            A1, A2, B1, B2
        Intransitive Verbs // do not require a direct object (e.g., "Je dors.").
            A1, A2, B1, B2
    Compoound Tense Logic
        Agreement Rules
            Agreement with être verbs (gender/number).
            Agreement with avoir verbs when the direct object precedes the verb.
            Agreement with reflexive verbs (which can be tricky!).
        Auxiliary Choice // Which verbs take Avoir vs. Être (The "Dr. Mrs. Vandertramp" list).  
            Avoir
                A1, A2, B1, B2
            Être
                A1, A2, B1, B2
    Verbal Structures (semi-auxiliary verbs)
        Modal Verbs // Vouloir, Pouvoir, Devoir + Infinitive. (e.g., "Je peux parler français.").
        Recent Past: Venir de + Infinitive.
        Continuous Present: Être en train de + Infinitive.
