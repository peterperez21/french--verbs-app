1. Sentence Modality (The "Type")
This defines the overall "flavor" of the sentence.
    Declarative: The standard Subject + Verb + Object.
    Interrogative: The three levels of questions (Intonation, Est-ce que, Inversion).
    Imperative: Commands (which uniquely drop the subject pronoun).
    Exclamatory: Expressing strong emotion (using Quel, Que, Comme).
2. The Negation "Sandwich"
French negation is modular. You have the "Base" (ne) and the "Qualifier" (pas, plus, jamais, rien).
    Simple Tense Logic: Ne + [Verb] + Pas.
    Compound Tense Logic: Ne + [Auxiliary] + Pas + [Participle].
    Infinitive Logic: Ne pas + [Infinitive] (they stay together!).
3. Pronoun Placement (The "Pre-Verb Slot")
This is the biggest hurdle for B-level developers. In English, pronouns come after the verb ("I see it"). In French, they move before the verb ("Je le vois").
    The Ordering Rule: If you have multiple pronouns (Je le lui donne), there is a strict "rank" they must follow.
    The Imperative Exception: In positive commands, they move back to the end (Donne-le-moi !).
4. Connectors & Complex Clauses (B1-B2)
This is where you handle how two sentences join together.
    Relative Pronouns: Qui, Que, Dont, Où.
    Conjunctions: Parce que, Bien que (which might trigger the Subjunctive in your Verb Schema), Puisque.
