Level A1
    Lesson 1
        Kickstart your French journey by mastering the art of saying hello and introducing yourself—whether you’re meeting someone formally or chatting with friends.
        Build a strong foundation with essential vocabulary and get comfortable counting from 1 to 10.
        It’s your first step toward confident French conversations!
    Lesson 2
        Give your greetings a fresh twist and take your small talk to the next level by learning how to describe the weather and seasons in French.
        Get to grips with the days of the week so you can confidently plan your schedule.
        Plus, sharpen your number skills up to 20, and start using ordinal numbers to bring clarity and precision to your conversations.
    Lesson 3
        Learn how to express how you feel, from heureux (happy) to fatigué (tired), and start making emotional connections in French.
        Discover the months of the year so you can talk about birthdays, holidays, and seasons with ease.
        You'll also learn how to talk about dates confidently—so you'll never miss an appointment again!
        And to top it all off, you'll build your number skills all the way up to 100, and even take your first steps towards 1000!
    Lesson 4
        Lay the groundwork for clear and confident communication by mastering the French alphabet.
        Learn how to spell your name and other basic words, and unlock the secrets of French pronunciation by tackling common letter combinations.
        Start sounding more like a native, one syllable at a time!
    Lesson 5
        Speak with accuracy and flair by learning when and how to use French articles.
        Understand definite articles to talk about specific things, get comfortable with indefinite articles for general items, and grasp the nuance of partitive articles to express “some” or “a bit of” something.
        Small words, big impact!
    Lesson 6
        Take control of your sentences with essential subject pronouns and two of French’s most powerful verbs: avoir (to have) and être (to be).
        Add colour to your communication by exploring adjective agreement and word order—start describing like a pro!
    Lesson 7    
        Connect ideas smoothly with everyday French conjunctions, share personal details like your birthday and age, and bring your descriptions to life with colours and expanded vocabulary.
        Learn to describe yourself and others with confidence and style.
    Lesson 8
            Master the essential verb faire (to do/make) and key expressions that go with it.
        Learn the difference between faire and jouer (to play) and build more natural sentences using common time expressions.
        Start talking about your activities and when you do them—all in French!
    Lesson 9
        Get moving in French by learning to conjugate aller (to go) in the present tense.
        Use prepositions to describe locations and directions with ease.
        Whether you're heading to the park or describing where something is, you’ll be equipped to talk about movement and place confidently.
    Lesson 10
        Start navigating the world of French verbs by learning about infinitives, verb moods, and the three main verb groups.
        Build the foundation for all future verb work by mastering how to conjugate regular Group 1 and Group 2 verbs in the present tense.
    Lesson 11
        Tackle the trickier but oh-so-important irregular Group 3 verbs and boost your fluency.
        Get to know impersonal verbs like il faut, and master the expression être en train de to describe actions in progress.
        You'll also learn the imperative mood to give simple, effective commands.
    Lesson 12
        Make your French more complete by learning how to say non!
        Master basic negation patterns in the present tense and flip affirmative sentences into negatives with ease—an essential step toward full expression.
Level A2
    Lesson 13
        Refine your descriptive skills by revisiting how French adjectives work with nouns.
        Learn to compare things clearly with comparative structures, and take it further by forming simple superlatives—your vocabulary just got an upgrade!
    Lesson 14
        Say whose is whose with possessive adjectives and point things out clearly using demonstrative adjectives.
        Whether it’s my book or that bag over there, you’ll learn how to add clarity and ownership to your French.
    Lesson 15
        Step into the past with confidence using the passé composé, one of the most important tenses in French.
        Learn to conjugate regular and irregular verbs and understand when to use avoir or être as your helping verb.
    Lesson 16
            Go beyond yes or no!
    Learn the three main ways to ask questions in French—intonation, est-ce que, and inversion—and start having richer, more interactive conversations.
    Lesson 17
            Build your vocabulary to describe the rooms and items in your home.
    Use essential prepositions to explain where things are—whether the chair is next to, under, or behind, you’ll be able to say it in French.
    Lesson 18
                Tame the quirks of French plurals by identifying irregular forms in both nouns and adjectives.
    Speak and write with greater accuracy as you tackle one of the trickier bits of grammar with confidence.
    Lesson 19
                Make your French more expressive and precise with adverbs!
    Learn how they modify verbs, adjectives, and even other adverbs, and understand their typical placement in sentences.
    Add nuance with ease!
Level B1
    Lesson 20
        Start expressing upcoming plans and intentions using the futur proche (near future).
        You’ll also learn how to form the participe présent (present participle) to describe simultaneous actions—a key step toward fluid storytelling and richer expression.
    Lesson 21
        Talk about future plans, dreams, and predictions with confidence using the futur simple.
        Learn how to conjugate regular and irregular verbs across all three groups, and begin expressing longer-term ideas with clarity and flair.
    Lesson 22
        Dive into daily routines and personal care with reflexive verbs.
        Learn how to conjugate them in the présent, futur proche, futur simple, and passé composé, and talk about what you do to yourself—from waking up to getting dressed.
    Lesson 23
        Learn to set the scene and describe habits from the past using the imparfait.
        Understand how it contrasts with the passé composé, and start painting vivid pictures of memories, stories, and ongoing past events.
    Lesson 24
        Take your French to the next level by linking ideas more smoothly. Master simple relative pronouns like qui, que, quoi, dont, and où to create more complex, natural-sounding sentences and avoid repetition.
    Lesson 25
        Get precise with possessive pronouns, point things out with demonstrative pronouns, and add emphasis where it counts using emphatic pronouns.
        These little words help you pack more meaning into every sentence.
    Lesson 26
        Clarify who is doing what to whom!
        Learn the difference between direct and indirect object pronouns, and how to place them correctly in different sentence structures.
        Speak and write more fluently and naturally.
    Lesson 27
        Talk about what would happen in a given situation using the conditionnel présent.
        Learn to express politeness, possibilities, and hypothetical situations—along with how to translate modal verbs like could, should, and would.
    Lesson 28
        Go beyond ne…pas!
        Expand your toolbox with complex negations including double negatives, negative pronouns, and negations in compound tenses, reflexives, and imperatives.
        Say “no” like a native.
    Lesson 29
        Level up your comparison skills with more sophisticated comparative structures and learn how to express subtle differences in people, objects, and ideas.
        Master superlatives for when you want to say something’s the best or the most important!
Level B2
    Lesson 30
        Enter the world of nuance with the subjunctive mood.
    Learn when and why it’s used, how to conjugate regular verbs, and discover common expressions and verbs that trigger the subjunctive.
    It’s time to express doubt, desire, and emotion like a pro.
    Lesson 31
        Deepen your understanding of the subjunctive mood by learning irregular conjugations and how to use it in interrogative and negative contexts.
        Explore the subjonctif passé (past subjunctive) and wrap your head around the subtle ne explétif. Subtlety, meet fluency.
    Lesson 32
        Go beyond basic phrases with compound and complex prepositions that express nuanced relationships between ideas.
        Whether you’re talking about cause, consequence, or contrast, this lesson equips you with the right expressions for more sophisticated communication.
    Lesson 33
        Add elegance and clarity to your sentences with complex relative pronouns like duquel, auquel, desquels, auxquels, and their feminine and neutral forms.
        Learn to avoid repetition and build precise, flowing sentences that show off your growing mastery.
    Lesson 34
        Express actions that happened before other past events with the plus-que-parfait.
        Master its formation and usage to create clear, chronological narratives and sharpen your storytelling in French.
    Lesson 35
        Look ahead with precision using the futur antérieur, a tense used to describe actions that will have been completed in the future.
        Learn to express assumptions, future deadlines, and logical conclusions with style.
    Lesson 36
        Talk about what would have happened in the past with the conditionnel passé.
        Learn how to form it using both avoir and être, and use it to express regrets, missed opportunities, and hypothetical past scenarios.